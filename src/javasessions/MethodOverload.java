public class MethodOverload {
	public void start() {
		System.out.println("start");
	}
	public void start(int i) {
		System.out.println("start");
	}
	public void start(int i,String y) {
		System.out.println("start");
	}
	public void start(String y,int i) {
		System.out.println("start");
	}
	

	public static void main(String[] args) {
		

	}

}
